
# -------------------------------------------------------------------------

              # Extract and Manipulate Parts of Dates #

# -------------------------------------------------------------------------

      # The functions provided by lubridate to perform extraction and 
      # manipulation of dates include
      # 
      #         year()  *** year
      #         month()  *** month
      #         week()  *** week
      #         yday()  *** day of the year
      #         mday()  *** day of the month
      #         wday()  *** day of the week
      #         hour()  *** hour
      #         minute()  *** minute
      #         second()  *** second
      #         tz()  *** time zone

# -------------------------------------------------------------------------

library (lubridate)

# -------------------------------------------------------------------------

x <- c ("2015-07-01", "2015-08-01", "2015-09-01")

# -------------------------------------------------------------------------

        # to extract the year 
year(x)

# -------------------------------------------------------------------------

        # month - default is number 

# -------------------------------------------------------------------------

month(x)

# -------------------------------------------------------------------------

      # show abbreviated name
month(x, label = TRUE, abbr = TRUE)


# -------------------------------------------------------------------------

wday(x, label = TRUE, abbr = FALSE)

# -------------------------------------------------------------------------

# convert to date format
x <- ymd(x)

# -------------------------------------------------------------------------

mday(x)
# change the days for the dates
mday(x) <- c(3, 10, 22)

# -------------------------------------------------------------------------

mday(x)
x

# -------------------------------------------------------------------------

            # can also use update () function
update(x, year= c(2013, 2014, 2015), month=9)

# -------------------------------------------------------------------------

            # can also add/subtract units
            # add a year (1yr) to the year
            # subtract 2, 9, 21 from the days of the year 2013, 2014, 2015
x + years(1) - days( c(2, 9, 21))

