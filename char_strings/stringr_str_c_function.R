
# -------------------------------------------------------------------------

                      # String Manipulation with stringr      # 

# -------------------------------------------------------------------------

library(stringr)

# -------------------------------------------------------------------------

# str_c () is equivalent to the paste () functions:# 

# -------------------------------------------------------------------------

# same as paste0 ()
str_c ("Learning", "to", "use", "the", "stringr", "package")

# -------------------------------------------------------------------------
# same as paste ()
str_c ("Learning", "to", "use", "the", "stringr", "package", sep = " ")

# -------------------------------------------------------------------------

# allows recycling
str_c (letters, " is for", "...")
