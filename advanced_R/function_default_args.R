
# -------------------------------------------------------------------------

            # Default arguments

# -------------------------------------------------------------------------

# default values can be defined in terms of other arguments, 
#or even in terms of variables defined later in the function:

# -------------------------------------------------------------------------

h04 <- function(x = 1, y = x * 2, z = a + b) {
  a <- 10
  b <- 100
  
  c(x, y, z)
}
h04()


# -------------------------------------------------------------------------


h05 <- function(x = ls()) {
  a <- 1
  x
}
h05()
h05(ls())

