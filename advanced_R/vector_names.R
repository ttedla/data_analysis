
# -------------------------------------------------------------------------

            # vector names

# -------------------------------------------------------------------------

        # vectors can be named in one of three ways

# -------------------------------------------------------------------------

        # 1. When creating 

# -------------------------------------------------------------------------

x <- c(a=1, b=2, c=3)
x
is.vector(x)

# -------------------------------------------------------------------------

        # 2. by modifying an existing vector in place 

# -------------------------------------------------------------------------

names(x) <- c("a", "b", "d")
x

# -------------------------------------------------------------------------

x <- 1:3; names(x) <- c("a", "b", "c")
x

# -------------------------------------------------------------------------

            # 3. by creating a modified copy of a vector 

# -------------------------------------------------------------------------


y <- c(1,3,5)
y
y <- setNames(1:3, c("A", "B", "C"))
y

# -------------------------------------------------------------------------

y <- c(a=1,2,3)
y
names(y)
v <- c(1,2,3)
v
names(v) <- c('a')
v
names(v)

z <- c(1,2,3)
names(z)

