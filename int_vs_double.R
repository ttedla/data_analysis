                # Integers vs. Doubles #

# -------------------------------------------------------------------------

        # To create a vector of integers using c() # 
        # you must specify explicity by placing an #
        # L directly after each number.            #  

dbl_var <- c(1, 2, 4)
typeof(dbl_var)

int_var <- c(1L, 2L, 4L)
typeof(int_var)
