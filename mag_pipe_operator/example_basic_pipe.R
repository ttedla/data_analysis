# Magrittr's Pipe Operator and Placeholders ( (%>%))

# -------------------------------------------------------------------------
        # consider the following example.

# -------------------------------------------------------------------------
      # very complex and mess to decipher
round(cos(exp(sin(log10(sqrt(25))))),2)

# -------------------------------------------------------------------------

      # Using the (%>%) operator

# -------------------------------------------------------------------------

library(magrittr)

# -------------------------------------------------------------------------
        # visually simple to decipher
sqrt(25) %>%
  log10() %>%
  sin() %>%
  exp() %>%
  cos() %>%
  round(2)
          
