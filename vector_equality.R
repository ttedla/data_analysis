x <- c (1, 4, 9, 12)
y <- c (4, 4, 9, 13)

# -------------------------------------------------------------------------

# Vector comparision # 

x == y

# -------------------------------------------------------------------------

# To count the number of equal vectors between the two


# -------------------------------------------------------------------------

sum(x==y)

# -------------------------------------------------------------------------

# To identify index of the 2 equal vectors 

# -------------------------------------------------------------------------

which(x==y)

# -------------------------------------------------------------------------
equal.vec <- function(x, y)
{
  for (i in which(x == y))
  {
    print(x[i])
  }
}
equal.vec(x,y)

