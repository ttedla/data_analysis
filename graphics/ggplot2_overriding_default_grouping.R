library(ggplot2)

# -------------------------------------------------------------------------
data(Oxboys, package = "nlme")

# -------------------------------------------------------------------------
ggplot(Oxboys, aes(Occasion, height))+
  geom_boxplot()

# -------------------------------------------------------------------------

        # to add a line that connects each individual boy#

# -------------------------------------------------------------------------

ggplot(Oxboys, aes(Occasion, height)) + 
  geom_boxplot() +
  geom_line(aes(group=Subject), colour = "#3366FF", alpha = 0.5)

