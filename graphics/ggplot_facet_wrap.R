library(ggplot2)

# -------------------------------------------------------------------------

          # There are two types of facetting: grid and wrapped.#

# -------------------------------------------------------------------------

# To facet a plot you simply add a facetting specification with facet wrap(),
# which takes the name of a variable preceded by ~ .

# -------------------------------------------------------------------------

ggplot(mpg, aes(displ, hwy)) + 
  geom_point() + 
  facet_wrap(~class)

