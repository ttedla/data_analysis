
# -------------------------------------------------------------------------

            # Adding On To Lists #

# -------------------------------------------------------------------------

# To add additional list components to a list we can leverage the 
# list () and append() functions.

# -------------------------------------------------------------------------

l1 <- list (1:3, "a", c (TRUE, FALSE, TRUE))

# -------------------------------------------------------------------------

str(l1)

# -------------------------------------------------------------------------

# If we add the new elements: 
# with list () it will create a list of two components,
# component 1 will be a nested list of the original list
# and component 2 will be the new elements added:

# -------------------------------------------------------------------------

l2 <- list (l1, c (2.5, 4.2))

# -------------------------------------------------------------------------

str(l2)

# -------------------------------------------------------------------------

# To simply add a fourth list component without creating nested lists
# we use the append() function:

# -------------------------------------------------------------------------

l3 <- append(l1, list(c(2.5, 4.2)))

# -------------------------------------------------------------------------

str(l3)

# -------------------------------------------------------------------------

# Alternatively, we can also add a new list component by utilizing 
# the '$' sign and naming the new item:

# -------------------------------------------------------------------------
l3$item4 <- "new list item"

# -------------------------------------------------------------------------

str(l3)

# -------------------------------------------------------------------------

str(l1)

# -------------------------------------------------------------------------

# To add additional values to a list item you need to subset for
# that specifi c list item and then you can use the c() function
# to add the additional elements to that list item:

# -------------------------------------------------------------------------

l1[[1]] <- c(l1[[1]], 4:6)

# -------------------------------------------------------------------------

str(l1)

# -------------------------------------------------------------------------
l1[[2]] <- c(l1[[2]], 'dding', 'to a', 'list')

# -------------------------------------------------------------------------

str(l1)

# -------------------------------------------------------------------------

                # Adding Attributes to Lists #

# -------------------------------------------------------------------------

# The attributes that you can add to lists include names, 
# general comments, and specific list item comments. 
# Currently, our l1 list has no attributes:

# -------------------------------------------------------------------------

attributes(l1)

# -------------------------------------------------------------------------

# adding names to a pre-existing list
names(l1) <- c("Item-1", "Item-2", "Item-3")

# -------------------------------------------------------------------------

str(l1)

# -------------------------------------------------------------------------

attributes(l1)

# -------------------------------------------------------------------------

# adding names when creating lists
l2 <- list (item1 = 1:3, item2 = letters[1:5], item3 = c (T, F, T, T))

# -------------------------------------------------------------------------

str(l2)

# -------------------------------------------------------------------------

attributes(l2)

# -------------------------------------------------------------------------

# With lists, we can add a general comment to the list using comment()
# and we can also add comments to specific list items with attr () .

# -------------------------------------------------------------------------

# adding a general comment to list l2 with comment ()
comment(l2) <- "This is a comment on a list"

# -------------------------------------------------------------------------

str(l2)

# -------------------------------------------------------------------------

# adding a comment to a specifi c list item with attr ()
attr (l2, "item2") <- "Comment for item2"

# -------------------------------------------------------------------------

str(l2)
