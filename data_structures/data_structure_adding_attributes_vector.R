
# -------------------------------------------------------------------------

          # Adding attributes to vectors # 

# -------------------------------------------------------------------------

v1 <- 8:17

# -------------------------------------------------------------------------

attributes(v1)

# -------------------------------------------------------------------------

# assigning names to a pre-existing vector
names(v1) <- letters[1:length(v1)]
v1

# -------------------------------------------------------------------------

attributes(v1)

# -------------------------------------------------------------------------

# adding names when creating vectors
v2 <- c (name1 = 1, name2 = 2, name3 = 3)
v2

# -------------------------------------------------------------------------

attributes(v2)

# -------------------------------------------------------------------------

# We can also add comments to vectors to act as a note to the user. 
# This does not change how the vector behaves; rather, it simply acts
# as a form of metadata for the vector.

# -------------------------------------------------------------------------

comment (v1) <- "This is a comment on a vector"
v1
attributes(v1)

