x <- c (1, 1.35, 1.7, 2.05, 2.4, 2.75, 3.1, 3.45, 3.8, 4.15,
        4.5, 4.85, 5.2, 5.55, 5.9)

# -------------------------------------------------------------------------

# Round to the nearest integer
round(x)

# -------------------------------------------------------------------------

# Round up
ceiling (x)

# -------------------------------------------------------------------------

# Round down
floor (x)

# -------------------------------------------------------------------------

# Round to a specifi ed decimal
round (x, digits = 1)

